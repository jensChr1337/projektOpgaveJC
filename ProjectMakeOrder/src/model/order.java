package model;

public class order {

	private int orderNumber;
	private int date; 
	
	

	public void setOrderNumber() {
		this.orderNumber = orderNumber; 
	}
	
	public int getOrderNumber() {
		return orderNumber; 
	}
		
	public void setDate() {
		this.date = date; 
	}
	
	public int getDate() {
		return date; 
	}
	
	
	
	
	
}

	









